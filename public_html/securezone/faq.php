<div onClick="openClose('a8')" class="mainExpand">
  <div align="justify">What about soft bounces? </div>
              </div>
              <div id="a8" class="texter">
              <div align="justify">These are generally considered as email addresses that return error messages due to a full inbox quota, or where their mail server is down or experiencing issues. A few years ago, the distinction between hard and soft bounces was clear, i.e. soft bounces were OK as they were due to temporary issues. However, the treatment of hard and soft bounces is gradually converging as problems like “full inbox” generally means an email account is “dead”, as quotas are now massive even for free ISPs. We treat soft and hard bounces in the same way, as our clients ultimately want to achieve as clean a list as possible when using our service.</div>
              </div>
                                                

