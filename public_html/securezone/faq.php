  <div align="justify">How are email addresses checked?</div>
              </div>
              <div id="a3" class="texter">
                <div align="justify">We check each and every email at multiple levels as follows:<br /><br />
                  Email syntax validation: A complete syntactical email validation is performed following the current IETF standards, including RFC 1123, RFC 2821, RFC 2822, RFC 3490, RFC 3696, RFC 4291, RFC 5321, RFC 5322 and RFC 5336. It fully supports: quoted strings, quoted pairs, internationalized domain names (IDN) and mailboxes, as well as comments and domain literals.<br /><br />
                  Domain/DNS check: We validate the DNS records for each email address under test and flag the ones which pertain to invalid, non-working and parked domains.<br /><br />
                  Disposable addresses (DEA) detection:  We detect temporarily email addresses generated by certain free services on the Internet, collectively known as disposable email address providers, such as 10MinuteMail, GuerrillaMail, MailEater, Mailinator, SpamGourmet,TempInbox,TrashMail and many more.<br /><br />
                  Mail exchanger test: We detect email addresses which do not have a valid mail exchanger. Behind the scenes, we even support many complex configurations that, despite not being widely adopted anymore in the modern web, were used in the early Internet days and can still be found in some rare circumstances.<br /><br />
                  Mailbox existence check: Check if an email address exists without sending email messages or notifying the target; it does so by emulating the behavior of common mail exchangers and employing the SMTP protocol wisely. Our service can validate even the mailboxes handled by Yahoo!, a result which is always difficult to achieve because of the strict security rules the company follows.<br /><br />
                  Temporary unavailability detection: We detect the temporarily unavailability of the mailboxes under test (like mailbox over quota errors) and automatically attempt to validate each address multiple times at specific intervals, to avoid a condition known as grey-listing and give back exact validation results.<br /><br />
                Catch-all test: We detect email addresses handled by catch-all (or accept-all, or even wildcard) mail exchangers, which accept every possible address for their domain. </div>
